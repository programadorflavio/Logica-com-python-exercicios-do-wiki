print('Digite o dia da semana:1-Domingo, 2-Segunda, 3-Terça, 4-Quarta, 5-Quinta, 6-Sexta, 7-Sábado')
dia = int(input('Informe o dia da semana: '))
if dia == 1:
    print('Domingo')
if dia == 2:
    print('Segunda')
if dia == 3:
    print('Terça')
if dia == 4:
    print('Quarta')
if dia == 5:
    print('Quinta')
if dia == 6:
    print('Sexta')
if dia == 7:
    print('Sábado')
else:
    print('Valor inválido, digite[1, 2, 3, 4, 5, 6, 7]')